    private void jbSaludarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSaludarActionPerformed

        String nombre = jtfNombreUsuario.getText();

        if (nombre.isEmpty() || nombre.isBlank()) {
            String mensajeError = "Introduzca su nombre en el cuadro de texto";
            JOptionPane.showMessageDialog(rootPane, mensajeError, "ERROR", JOptionPane.ERROR_MESSAGE);
        } else {
            String mensaje = "Hola " + nombre + ".\n Bienvenido a mi primera aplicacion Swing.";
            jlSaludo.setText(mensaje);
            jlSaludo.setVisible(true);
        }


    }//GEN-LAST:event_jbSaludarActionPerformed

    private void jmiNuevoUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jmiNuevoUsuarioActionPerformed

        JDNuevoUsuario nuevoUsuario = new JDNuevoUsuario(this, true);
        nuevoUsuario.setVisible(true);

        if (nuevoUsuario.haAceptado()) {
            Usuario user = nuevoUsuario.getNuevoUsuario();
            this.jtfNombreUsuario.setText(user.getNombre());
        }

    }//GEN-LAST:event_jmiNuevoUsuarioActionPerformed

