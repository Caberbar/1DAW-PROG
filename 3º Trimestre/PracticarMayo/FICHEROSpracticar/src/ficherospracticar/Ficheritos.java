    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        JFileChooser jfc = new JFileChooser();
        
        jfc.setAcceptAllFileFilterUsed(false);
        FileNameExtensionFilter filtroExtension = new FileNameExtensionFilter("Solo archivos txt", "txt");
        jfc.addChoosableFileFilter(filtroExtension);
        
        int opcion = jfc.showOpenDialog(this);
        
        if (opcion == JFileChooser.APPROVE_OPTION) {
            String fichero = jfc.getSelectedFile().getAbsolutePath();
        } else {
            System.err.println("No se ha cargado ning√∫n archivo");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

