package DiscoDuroDeRoer;

//Declara dos variables numéricas (con el valor que desees), muestra por consola la suma, resta, multiplicación, división y módulo (resto de la división).

public class EjerciciosBásicos_1 {

    public static void main(String[] args) {
        int n = 5;
        int x = 2;
        System.out.println("La suma es: "+(n+x));
        System.out.println("La resta es: "+(n-x));
        System.out.println("La multiplicación es: "+(n*x));
        System.out.println("La división es: "+(n/x));
        System.out.println("El módulo es: "+(n%x));
    }
    
}
